#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*ptr;

	ptr = lst;
	if (ptr == 0)
		return (0);
	while (ptr->next != NULL)
		ptr = ptr->next;
	return (ptr);
}
